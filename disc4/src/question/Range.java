/**
 * Generates Range of (min,max) 
 * DO NOT MODIFY THIS CLASS
 */
package question;

public class Range {
	private int max;
	private int min;
	
	public Range(int min, int max){
		this.max = max;
		this.min = min;
	}
	public int getMax(){
		return max;
	}
	
	public int getMin(){
		return min;
	}
}
